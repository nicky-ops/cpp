// This program gives instructions on how to prepare Blueberry muffins to die for
#include <iostream>


// This function prints out to the screen the list of instructions, ingredients, kitchen utensils used in the preparation of blueberry muffins.
int main()
{
	std::cout<<R"(How to Prepare Blueberry Muffins to Die For:
Step 1:
Gather all the ingredients and utensils needed:
a.Muffins:
- 1.5 cups of all-purpose flour
- 0.75 cup white sugar
- 2 teaspoons baking powder
- 0.5 teaspoon salt
- 0.33 cup vegetable oil
- 1 large egg
- 0.33 cup milk, or more as needed
- 1 cup fresh blueberries

b. Crumb Topping:
- 0.5 cup white sugar
- 0.33 cup all-purpose flour
- 0.25 cup butter, cubed
- 1.5 teaspoons ground cinnamon

c. Utensils
- 8-cup muffin pan (if using a 12-cup muffin pan, add 1 tablespoon of water to the empty cup spaces when baking to avoid burning the muffins).
- Measuring cups
- Tablespoons and teaspoons
- Large bowl
- Fork
- Toothpick
- Oven

Step 2:
Preheat the oven to 400 degrees F(200 degrees C). Grease 8 muffin cups or in line with paper liners. Wait for the oven signal(beep or light off) that it has reached the target temperature before putting the pan in.

Step 3:
To make the muffins: Whisk flour, sugar, baking powder, and salt together in a large bowl.

Step 4:
Pour oil into a small liquid measuring cup. Add egg and enough milk to reach the 1-cup mark; stir with a spoon or fork until combined.

Step 5:
Pour into flour mixture and mix just until batter is combined. Fold in blueberries; set batter aside.

Step 6:
To make the crumb topping: Combine sugar, flour, butter and cinnamon in a small bowl. Mix with a fork until crumbly.

Step 7:
Spoon batter into the prepared muffin cups, filling right to the top. Sprinkle with crumb topping.

Step 8:
Bake in the preheated oven until a tootpick inserted in the center of a muffin comes out clean, 20 to 25 minutes. Take out the muffins and be sure to protect your hands using kitchen gloves when removing the muffins from the oven. Enjoy.

Glossary of terms:
Batter: semi-liquid mixture made from flour, liquid(water or milk), and eggs
Muffin pan: a rigid tray with multiple individual, cup-shaped cavities used to hold batter for baking
Oven: kitchen appliance designed to cook food to high temperatures in a sealed space.
Toothpick: a slender, pointed tool traditionally used for cleaning teeth, but used to test if batter has finished cooking in the oven in baking contexts
Toothpick test: used to test if the muffin is ready. done if it comes out clean or with a few dry crumbs and underbaked if it comes out with wet batter clinging to it.
)";
}
